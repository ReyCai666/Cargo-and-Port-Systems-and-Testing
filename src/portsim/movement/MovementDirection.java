package portsim.movement;

/**
 * Represents the possible directions a ship or cargo can be moving.
 */
public enum MovementDirection {
    /**
     * Enum constants
     *
     * @Constant INBOUND - Any cargo / ships that are coming to the \n
     * port are considered inbound.
     * @Constant OUTBOUND - Any cargo / ships that are moving away \n
     * from the port are considered outbound.
     */
    INBOUND, OUTBOUND
}
