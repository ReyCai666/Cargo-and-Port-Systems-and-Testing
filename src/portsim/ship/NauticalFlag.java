package portsim.ship;

/**
 * Stores information about particular nautical flags flown by ships.
 */
public enum NauticalFlag {
    /**
     * Enum Constants
     *
     * @Constant BRAVO - The ship is carrying dangerous cargo.
     * @Constant HOTEL - The ship is ready to be docked.
     * @Constant NOVEMBER - Default flag where ship has no specific status.
     * @Constant WHISKEY - Ship requires medical assistance.
     */
    BRAVO, HOTEL, NOVEMBER, WHISKEY

}
