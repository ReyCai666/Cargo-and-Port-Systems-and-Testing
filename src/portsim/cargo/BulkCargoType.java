package portsim.cargo;

/**
 * Represents the possible types that Bulk Cargo can be.
 */
public enum BulkCargoType {
    /**
     * Enum Constants
     *
     * @Constant COAL - A combustible black or brownish-black \n
     * used as a fuel source.
     * @Constant GRAIN - A small hard seed.
     * @Constant MINERALS - A solid compound, usually ores such as iron ore.
     * @Constant OIL - A viscous liquid derived from petroleum, \n
     * especially for use as a fuel or lubricant.
     * @Constant OTHER - Another form of bulk cargo that does \n
     * not fit into other categories.
     */
    COAL, GRAIN, MINERALS, OIL, OTHER
}
